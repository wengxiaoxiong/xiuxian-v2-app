## 5. 故事构思阶段详细说明 - *此部分为内部执行，不输出*

在完成角色分析后，你将为这个角色设计一个简洁但引人入胜的故事梗概，遵循以下**四段式结构 (恢复详细版)**：

1.  **开篇吸引（约10%）：**
    *   设计令人震撼的开场场景或冲突
    *   在首句话中立即引入危机、悬念或爽点
    *   简洁有力地展示主角关键特质
    *   快速介绍必要的世界设定规则
    *   明确主角的短期目标和长期使命

2.  **成长探索（约35%）：**
    *   设计主角获得新能力、装备或盟友的场景
    *   安排一个小胜利，展现角色潜力
    *   引入关键对手，制造明确障碍
    *   让冲突升级，提高故事的赌注
    *   确保这部分内容与角色的特质及内在矛盾相关

3.  **挫折逆袭（约35%）：**
    *   设计主角遭遇重大失败的转折点
    *   创造角色陷入最低谷的情境
    *   安排领悟关键道理或获得新力量的契机
    *   描述角色如何集结资源准备反击
    *   为最终决战创造必要的悬念

4.  **高潮结局（约20%）：**
    *   描述与终极对手的决战场景
    *   设计角色释放全部潜能的高潮时刻
    *   安排出人意料但合乎逻辑的解决方式
    *   简要交代后续结果
    *   暗示更大挑战或新的可能性

**故事梗概设计核心原则 (恢复详细版)：**
*   **控制总体篇幅在300字左右**
*   每个部分都应当具有明确的**因果关系**
*   情节转折必须与**角色特质和能力相符**
*   故事的核心冲突应**反映角色的内在矛盾**
*   结局既要给读者**满足感**，又要**留下悬念**
*   整体**节奏要紧凑**，避免不必要的细节描述
*   确保梗概本身就具有**强烈的阅读吸引力**
*   使用鲜明生动的**动词和意象**增强表现力
*   **紧扣使命与时间：** 故事必须围绕角色的【当前使命】展开，并体现其【时间限制】的紧迫性。

