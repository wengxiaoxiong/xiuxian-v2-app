出身于远古八族之一萧族的没落分支，定居加玛帝国乌坦城。父亲萧战为族长，母亲早逝（身世后期揭示与古族有关）。因灵魂穿越自地球，拥有两世记忆，灵魂强度与修炼天赋远超常人：4岁修炼斗气，10岁达九段斗之气，11岁凝聚气旋成为斗者，创家族百年记录。12岁时斗气莫名流失，三年内跌至斗之气三段，沦为"废物"。实为药老（药尘）灵魂寄宿其戒指吸收斗气所致。受尽族人冷眼，未婚妻纳兰嫣然借云岚宗之势登门退婚，萧炎愤而写下休书，立下"三年之约"并宣言"莫欺少年穷"。15岁时发现药老存在，拜其为师，以恢复实力为目标。初期使命仅为通过家族考核并兑现三年之约。

