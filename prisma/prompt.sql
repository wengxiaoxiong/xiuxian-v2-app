- 面容：相貌清秀，脸上带着懒散的笑容，漆黑色的双眸中有着绚丽的光泽涌动，看上去显得异常的温和以及深邃
- 体态：身躯修长，气质洒脱，在其身体表面，燃烧着绚丽的火焰
- 服饰：身着黑衣，斜背负着一把黑色大尺
- 特殊标志：在其额头处，有着一道火焰印记，呈现诸多绚丽之色
- 整体气质：远远看去，犹如是一尊掌控火焰的炎神，充满着莫名的威压

## 灵根属性
火灵根：先天火属性根骨，使萧炎对火系功法有极高亲和力，修炼速度是普通修士的1.2倍。火克金，被水克制，战斗中对金系修士能造成1.2倍伤害，但面对水系修士时处于劣势。作为"爆发型战修"，火灵根使萧炎掌握的火系法术威力惊人，能附加持续燃烧效果，但灵气消耗速度快，需大量火属性天材地宝辅助修炼。也因火灵根暴躁特性，萧炎比常人更易陷入心魔反噬，需不断锤炼意志抵抗火性入魔的危险。火灵根加持下，《焚决》修炼速度远超常人，也为他日后吞噬异火奠定了坚实基础。

