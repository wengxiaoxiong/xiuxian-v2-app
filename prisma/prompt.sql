## 6. 灵根设计原则
在为角色选择和描述灵根时，遵循以下原则：

1. 契合角色核心：
   - 灵根应与角色的性格、成长轨迹、战斗风格形成呼应
   - 灵根的优势和劣势要能推动角色发展，提供成长机会

2. 灵根与矛盾设计：
   - 考虑灵根如何形成角色的内在矛盾（如火灵根既带来强大爆发力，又增加心魔风险）
   - 设计角色如何应对或规避灵根带来的短板（如火灵根修士研发抗水系法术）

3. 灵根描述的具体元素：
   - 灵根类型：必须为五行灵根之一（金、木、土、水、火）
   - 克制关系：该灵根能克制和被克制的属性
   - 核心能力表现：该灵根带来的特殊能力和修炼倾向
   - 战斗风格影响：灵根如何塑造角色的战斗特点
   - 修炼资源需求：角色因灵根而特别需要的天材地宝类型

4. 避免灵根设计中的常见问题：
   - 不合理的"完美灵根"：避免设计没有缺陷的全能灵根以及变异灵根
   - 灵根与性格脱节：灵根特性应反映在角色性格和行为模式中
   - 灵根能力过于宽泛：每种灵根应有明确的擅长和不足领域

## 7. 角色时间点立体化技巧
为了创造在特定时间点真正立体的角色，特别注重以下几个方面：

1. 当下困境与抉择：
   - 设计角色在当前时间点面临的具体困境和决策点
   - 确保这些困境与角色的成长阶段、能力水平和环境相符

2. 现阶段关系动态：
   - 描述角色与周围人物的即时互动状态
   - 展现关系中的张力、期待或误解

3. 情绪状态与应对机制：
   - 刻画角色在当前阶段的主导情绪和心理状态
   - 描述其应对压力和挑战的特定方式

4. 能力断层与成长痕迹：
   - 明确角色已掌握和尚未掌握的能力边界
   - 展示能力发展中的不平衡和特点

